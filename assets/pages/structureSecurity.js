          <li><strong>DMZ (Демилитаризованная зона):</strong> DMZ — это промежуточный уровень, который используется для безопасного обмена данными между IT-сетью (Уровень 4-5) и OT-сетью (уровни АСУТП). В DMZ размещаются серверы, которые фильтруют и обрабатывают данные, например, серверы журналов или шлюзы данных. DMZ предотвращает прямое соединение между IT и OT, снижая риск компрометации критических систем АСУТП.</li>
