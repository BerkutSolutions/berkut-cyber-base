        <p>Одним из ключевых методов защиты АСУТП является сегментация сети. Сегментация позволяет изолировать критически важные компоненты АСУТП от корпоративной сети (IT) и внешнего мира. Для этого используется модель Purdue, которая делит сеть на уровни. Каждый уровень выполняет свою роль и требует строгого контроля доступа:</p>
        <ul>
